import math
class Rownania:
    def rownanie(numer,x):
        if rownanie == 1:
            return x ** 6 - 2 * x ** 4 + x ** 2 + 1
        elif rownanie == 2:
            return math.cos(x)
        elif rownanie == 3:
            return 2 ** x
        elif rownanie == 4:
            return (x**2 + 3*x + 2) * math.sin(x)
